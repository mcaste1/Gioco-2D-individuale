window.addEventListener("keydown", (event) => {
    let k = event.keyCode;
    setTimeout(() => {
        if (k == 37 || k == 65) {
            pacman.nextDirection = DIRECTION_LEFT;// a - freccia sinistra
        } else if (k == 38 || k == 87) {
            pacman.nextDirection = DIRECTION_UP;// w - freccia su
        } else if (k == 39 || k == 68) {
            pacman.nextDirection = DIRECTION_RIGHT;// d - freccia destra
        } else if (k == 40 || k == 83) {
            pacman.nextDirection = DIRECTION_BOTTOM;// s - freccia
        }
    }, 1);
});
